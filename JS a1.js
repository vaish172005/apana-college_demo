//Create variables using let and const keywords include firstName, lastName, Address, City, Country, aggandes as a variable and also print these using concatenation and template literals in js
let firstName = "John";
let lastName = "Doe";
let address = "123 Main St";
let city = "Nashik";
let country = "India";
const age = 30; // Using const for a variable that won't change

// String concatenation
console.log("Using concatenation:");
console.log("Name: " + firstName + " " + lastName);
console.log("Address: " + address);
console.log("City: " + city);
console.log("Country: " + country);
console.log("Age: " + age);

// Template literals
console.log("\nUsing template literals:");
console.log(`Name: ${firstName} ${lastName}`);
console.log(`Address: ${address}`);
console.log(`City: ${city}`);
console.log(`Country: ${country}`);
console.log(`Age: ${age}`);
